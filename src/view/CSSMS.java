    private void viewScheduleSalary3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewScheduleSalary3ActionPerformed
        staffTableModel = new StaffTableModel(managerController.getManager(), staffController.getStaff());
        staffTable.setModel(staffTableModel);
        staffTable.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                if (e.getClickCount() == 2) {
                    int row = staffTable.getSelectedRow();
                    int column = staffTable.getSelectedColumn();
                    if (row != -1 && column != -1) {
                        detailedShiftDialog.pack();
                        detailedShiftDialog.setLocationRelativeTo(null);
                        detailedShiftDialog.setVisible(true);
                        showShiftLabel.setText(staffTableModel.getTableController().getPersonalShiftArray()[row][column].getShiftId());
                        showDayLabel.setText(String.valueOf(staffTableModel.getTableController().getPersonalShiftArray()[row][column].getDate().getDay()));
                        showMonthLabel.setText(String.valueOf(staffTableModel.getTableController().getPersonalShiftArray()[row][column].getDate().getMonth()));
                        showYearLabel.setText(String.valueOf(staffTableModel.getTableController().getPersonalShiftArray()[row][column].getDate().getYear()));
                        showCampusLabel.setText(String.valueOf(staffTableModel.getTableController().getPersonalShiftArray()[row][column].getCampusId()));
                        showJobLabel.setText(String.valueOf(staffTableModel.getTableController().getPersonalShiftArray()[row][column].getJob()));
                    }
                }
            }
        });
        this.staffDialog.setVisible(false);
        nameLabel7.setText(currentUser.getName());
        this.viewStaffScheduleDialog.pack();
        this.viewStaffScheduleDialog.setLocationRelativeTo(null);
        this.viewStaffScheduleDialog.setVisible(true);
    }//GEN-LAST:event_viewScheduleSalary3ActionPerformed

