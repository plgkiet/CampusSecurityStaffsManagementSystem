    private void signInButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signInButtonActionPerformed
        String username = usernameTextField.getText();
        String password = new String(passwordField.getPassword());

        if (username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter username and password", "Login Error", JOptionPane.ERROR_MESSAGE);
        } else {
            User user = userManager.hasUser(username, password);
            if (user == null) {
                JOptionPane.showMessageDialog(this, "Username or Password does not match or exist");
            } else {
                JOptionPane.showMessageDialog(this, "Successfully Login");
                currentUser = user;
                if (user.getRole() == USERROLE.MANAGER) {
                    Manager manager = new Manager(user.getName(), user.getId(), user.getPassword());
                    nameLabel.setText(currentUser.getName());
                    idFillLabel.setText(currentUser.getId());
                    nameFillLabel.setText(currentUser.getName());
                    roleFillLabel.setText("MANAGER");
                    managerController = new ManagerController(manager);
                    managerController.getManager().setScheduleMap(dao.loadSchedule());
                    this.setVisible(false);
                    this.loadingDialog.pack();
                    this.loadingDialog.setLocationRelativeTo(null);
                    this.loadingDialog.setVisible(true);

                    // Set up a Timer to delay for 1 second
                    javax.swing.Timer timer = new javax.swing.Timer(1000, new ActionListener() {
                        @Override
                        public void actionPerformed(ActionEvent e) {
                            loadingDialog.setVisible(false);
                            managerDialog.pack();
                            managerDialog.setLocationRelativeTo(null);
                            managerDialog.setVisible(true);
                        }
                    });
                    timer.setRepeats(false); // Only execute once
                    timer.start();

                } else if (user.getRole() == USERROLE.STAFF) {
                    managerController = new ManagerController(new Manager("kiet", "1", "0"));
                    Staff staff = managerController.getManager().getStaffList().get(user.getId());
                    staffController = new StaffController(staff);
                    staff.setPersonalShifts(dao.loadPersonalShiftFromDatabase(user.getId()));
                    managerController.getManager().setScheduleMap(dao.loadSchedule());
                    nameLabel1.setText(currentUser.getName());
                    idFillLabel1.setText(currentUser.getId());
                    nameFillLabel1.setText(currentUser.getName());
                    roleFillLabel1.setText("STAFF");
                    staffController = new StaffController(staff);
                    this.setVisible(false);
                    this.loadingDialog.pack();
                    this.loadingDialog.setLocationRelativeTo(null);
                    this.loadingDialog.setVisible(true);

                    // Set up a Timer to delay for 1 second
                    javax.swing.Timer timer = new javax.swing.Timer(1000, new ActionListener() {
                        @Override
                        public void actionPerformed(ActionEvent e) {
                            loadingDialog.setVisible(false);
                            nameLabel1.setText(currentUser.getName());
                            staffDialog.pack();
                            staffDialog.setLocationRelativeTo(null);
                            staffDialog.setVisible(true);
                        }
                    });
                    timer.setRepeats(false); // Only execute once
                    timer.start();
                }
            }
        }


    }//GEN-LAST:event_signInButtonActionPerformed

