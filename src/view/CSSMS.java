    private void registerStaffButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerStaffButtonActionPerformed
        String id = idTextField.getText();
        String name = nameTextField.getText();
        String password = new String(staffPasswordField.getPassword());
        String password1 = new String(staffPasswordField1.getPassword());
        if (id.equalsIgnoreCase("") || name.equalsIgnoreCase("") || password.equalsIgnoreCase("") || password1.equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(this, "Please fill all information", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            if (!password.equals(password1)) {
                passwordMatchingNotificationLabel.setText("Password does not matching");
            } else {
                if (managerController.registerStaff(name, id, password, userManager)) {

                    JOptionPane.showMessageDialog(this, "Successfully Registering");
                    dao.insertStaffData(id, name, password, "STAFF", 0, 0);
                } else {
                    JOptionPane.showMessageDialog(this, "Identity number exists", "Error Registering", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_registerStaffButtonActionPerformed

